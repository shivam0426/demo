public class keyword {

    public static void main(String[] args) {

        // ============================================================
        //                          JAVA KEYWORDS
        // ============================================================
        // ➤ Keywords are reserved words in Java.
        // ➤ They have special meaning for the compiler.
        // ➤ They CANNOT be used as identifiers (variable names, class names, etc.).


        // ---------------------- Examples in Code ----------------------

        // 'int' → keyword for integer data type
        int intKeyword = 10;

        // 'float' → keyword for floating-point data type
        float floatKeyword = 20.5f;

        // 'if' → keyword used for conditional branching
        if (intKeyword < floatKeyword) {
            System.out.println("intKeyword is less than floatKeyword");
        }


        // ============================================================
        //                  LIST OF IMPORTANT JAVA KEYWORDS
        // ============================================================

        // ----------- 1. Data Type Keywords ---------------
        // byte, short, int, long, float, double, char, boolean

        // ----------- 2. Access Modifiers -----------------
        // public, private, protected

        // ----------- 3. Class & Object Related -----------
        // class, interface, extends, implements, this, super, new, package, import

        // ----------- 4. Control Flow Keywords ------------
        // if, else, switch, case, default
        // for, while, do
        // break, continue, return

        // ----------- 5. Exception Handling Keywords -------
        // try, catch, finally, throw, throws

        // ----------- 6. Boolean/Value Keywords ------------
        // true, false, null

        // ----------- 7. Miscellaneous Keywords ------------
        // static, final, abstract, synchronized, volatile, transient
        // enum, instanceof, assert

        // NOTE:
        // There are 67 keywords in Java (including literals), depending on version.
        // These words cannot be used as variable names or identifiers.

    }
}
